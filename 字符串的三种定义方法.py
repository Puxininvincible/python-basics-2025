#单引号定义法
name='黑马程序员'
print(type(name))
#双引号定义发
name="黑马程序员"
print(type(name))
#三引号定义发
name="""
我是
黑马
程序员
"""
print(type(name))

#在字符串内 包含双引号
name='"黑马程序员"'
print(name)
#在字符串内 包含单引号
name="'黑马程序员'"
print(name)
#使用转义字符\解除引号的效用
name="\"黑马程序员\""
print(name)
name='\'黑马程序员\''
print(name)